package DecisionStructures;

public class Stars {
	public static void main(String args[]) {
		for (int i =0; i< 3; i++) {
			if (i == 0) {
				System.out.println("   *  ");
			}
			else if (i ==1) {
				System.out.println("  * * ");
			}
			else if (i == 2) {
				System.out.println(" * * *");
			}
		}
	}
}
