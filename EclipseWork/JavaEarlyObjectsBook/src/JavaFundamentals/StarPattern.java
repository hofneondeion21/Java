package JavaFundamentals;

public class StarPattern {
	public static void main(String args[]) {
		String 	oneStar = "*", 
				threeStar = "***", 
				fiveStar = "*****", 
				septStar = "*******";
		
		
		System.out.printf("   %s\n  %s\n %s\n%s\n %s\n  %s\n   %s", oneStar, threeStar, fiveStar, septStar, fiveStar,
				threeStar, oneStar);
	} // endae main
} // endae CLASS
